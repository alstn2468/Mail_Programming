# 등비수열은 앞 수와 뒤의 수가 일정한 비를 가지는 수열입니다.
# 예를들어 3, 6, 12는 3 x 2 = 6, 6 x 2 = 12로 일정하게 두 배씩 늘어나기 때문에 등비수열을 만족합니다.
#
# Input: [1, 2, 6, 10, 18, 54]
# Output:
# 2 6 18
# 6 18 54
#
# Input: [2, 8, 10, 15, 16, 30, 32, 64]
# Output:
# 2 8 32
# 8 16 32
# 16 32 64
#
# Input: [1, 2, 6, 18, 36, 54]
# Output:
# 2 6 18
# 1 6 36
# 6 18 54
#
# Input: [1, 2, 4, 16]
# Output:
# 1 2 4
# 1 4 16
#
# Input: [1, 2, 3, 6, 18, 22]
